ISSUE_CANONICAL_PATTERNS = [
    (r"burst pipe|canalis.*(éclat|perc)|pipe.*burst|évier.*(éclat|perc)", "burst_pipe"),
    (r"clogged toilet|toilet.*(clog|block|bouch)|wc bouch", "clogged_toilet"),
    (r"clogged drain|drain.*(clog|block)|évier bouch|évacuation bouch", "clogged_drain"),
    (r"water heater|chauffe-eau|boiler", "water_heater"),
    (r"\b(flood|inond)", "flooding"),
    (r"\b(fuite|leak|drip|goutte)", "leak"),
    (r"\b(toilet|wc)\b", "toilet"),
    (r"\b(pipe|tuyau|canalis)", "pipe_issue"),
    (r"general inquiry|demande gén", "general_inquiry"),
]


def canonicalize_issue(issue_type):
    """Map free-text or legacy issue_type to a canonical slug."""
    if not issue_type:
        return "general_inquiry"
    raw = issue_type.strip().lower().replace(" ", "_").replace("-", "_")
    if raw in {
        "general_inquiry", "leak", "clogged_drain", "clogged_toilet",
        "water_heater", "toilet", "pipe_issue", "burst_pipe", "flooding",
    }:
        return raw
    lower = issue_type.lower()
    for pattern, slug in ISSUE_CANONICAL_PATTERNS:
        if re.search(pattern, lower):
            return slug
    return "general_inquiry"


def issue_label(issue_type, lang=None):
    if not issue_type:
        return "—"
    slug = canonicalize_issue(issue_type)
    key = f"issue.{slug}"
    translated = translate(key, lang)
    if translated != key:
        return translated
    return issue_type


def get_lang():
    # Robust outside a request/app context (e.g. background jobs, the voice
    # pipeline building a devis): fall back to the default language instead of
    # raising "Working outside of request context".
    try:
        if hasattr(g, "lang") and g.lang:
            return g.lang
        cookie_lang = request.cookies.get("lang") if request else None
        if cookie_lang in SUPPORTED_LANGUAGES:
            return cookie_lang
        session_lang = session.get("lang")
        if session_lang in SUPPORTED_LANGUAGES:
            return session_lang
        accept = request.accept_languages.best_match(SUPPORTED_LANGUAGES) if request else None
        return accept or DEFAULT_LANGUAGE
    except RuntimeError:
        return DEFAULT_LANGUAGE


def translate(key, lang=None, **kwargs):
    lang = lang or get_lang()
    text = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key)
    if text is None:
        text = TRANSLATIONS["en"].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


